<h3 id="club-history">Club History</h3>
<p class="p">
    The Christchurch Chess Club was founded in 1866 for a telegraph match against the Nelson Chess Club 
    activity then reduce until 1877, when the club prepared to host the 1879 NZ Chess Championships. 
    Following the Championships, some disgruntled members left the club, soon after the Canterbury Chess 
    Club was born on the 18th of October, 1879. As for the Christchurch Chess Club, nearly the whole membership 
    had gone to Canterbury Chess Club by the end of 1879. Although there is a date for the formation of the 
    Canterybury Chess Club as a theoretically seperate entity, the club centerary and sesquicentenary were 
    celebrated in 1966 and 2016 respectively as the history is closer to a name change than two rival clubs.
</p>

<h3 class="mt-4" id="who-are-we">Who Are We?</h3>
<p class="p">
    We organise tournaments and social chess, all equipment and refreshments provided. Anyone can come to our 
    social nights on Thursdays, and beginners can be shown how to play. Wednesday tournaments use chess clocks 
    and are rated by NZ Chess, and players must be members of the club to enter.
</p>

