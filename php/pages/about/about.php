		<h2>Club History</h2>
        <p>The Christchurch Chess Club was founded in 1866 for a telegraph match against the Nelson Chess Club activity then reduce until 1877, when the club prepared to host the 1879 NZ Chess Championships. Following the Championships, some disgruntled members left the club, soon after the Canterbury Chess Club was born on the 18th of October, 1879. As for the Christchurch Chess Club, nearly the whole membership had gone to Canterbury Chess Club by the end of 1879.</p>
